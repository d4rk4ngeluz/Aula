valor = float(input("Digite o valor a ser calculado: "))

dobro = valor * 2
triplo = valor * 3
quadrado = valor **2
raiz_quadrada = valor**(1/2)
raiz_cubica = valor**(1/3)

print (f"O dobro do valor é: {dobro}")
print (f"O triplo do valor é: {triplo}")
print (f"O quadrado do valor é: {quadrado}")
print (f"O raiz_quadrada do valor é: {raiz_quadrada}")
print (f"O raiz_cubica do valor é: {raiz_cubica}")